#!/usr/bin/env bash
set -euo pipefail

# 实验前环境准备脚本（Ubuntu）
# 目的：
# 1) 固定到高性能电源策略，降低频率波动
# 2) 可选关闭 SMT/超线程，减少同核资源竞争
# 3) 可选把实验进程限制在 P-core，避免 P/E 混跑抖动
#
# 用法示例：
#   ./scripts/prepare_benchmark_env.sh
#   ./scripts/prepare_benchmark_env.sh --disable-smt
#   ./scripts/prepare_benchmark_env.sh --disable-smt --write-env-file
#
# 说明：
# - 默认只做“安全配置”（不改 SMT）
# - 关闭 SMT 需要 root，会影响全机调度行为

DISABLE_SMT=0
WRITE_ENV_FILE=0
ENV_FILE="scripts/benchmark_env.sh"

while [[ $# -gt 0 ]]; do
  case "$1" in
    --disable-smt)
      DISABLE_SMT=1
      shift
      ;;
    --write-env-file)
      WRITE_ENV_FILE=1
      shift
      ;;
    -h|--help)
      sed -n '1,40p' "$0"
      exit 0
      ;;
    *)
      echo "Unknown option: $1" >&2
      exit 2
      ;;
  esac
done

need_cmd() {
  command -v "$1" >/dev/null 2>&1 || {
    echo "Error: missing command '$1'" >&2
    exit 1
  }
}

need_cmd lscpu

run_root() {
  # root 直接执行；非 root 使用 sudo
  if [[ "${EUID}" -eq 0 ]]; then
    bash -c "$1"
  else
    sudo bash -c "$1"
  fi
}

echo "== 1) CPU 拓扑概览 =="
lscpu | sed -n '1,25p'
echo

echo "== 2) 当前策略状态 =="
GOV_NOW="unknown"
PSTATE_NOW="unknown"
PROFILE_NOW="unknown"
SMT_CTRL_NOW="unknown"
SMT_ACTIVE_NOW="unknown"

if [[ -r /sys/devices/system/cpu/cpufreq/policy0/scaling_governor ]]; then
  GOV_NOW="$(cat /sys/devices/system/cpu/cpufreq/policy0/scaling_governor)"
fi
if [[ -r /sys/devices/system/cpu/intel_pstate/status ]]; then
  PSTATE_NOW="$(cat /sys/devices/system/cpu/intel_pstate/status)"
fi
if command -v powerprofilesctl >/dev/null 2>&1; then
  PROFILE_NOW="$(powerprofilesctl get || true)"
fi
if [[ -r /sys/devices/system/cpu/smt/control ]]; then
  SMT_CTRL_NOW="$(cat /sys/devices/system/cpu/smt/control)"
fi
if [[ -r /sys/devices/system/cpu/smt/active ]]; then
  SMT_ACTIVE_NOW="$(cat /sys/devices/system/cpu/smt/active)"
fi

echo "scaling_governor : ${GOV_NOW}"
echo "intel_pstate     : ${PSTATE_NOW}"
echo "power_profile    : ${PROFILE_NOW}"
echo "smt/control      : ${SMT_CTRL_NOW}"
echo "smt/active       : ${SMT_ACTIVE_NOW}"
echo

echo "== 3) 设置 performance 电源策略 =="
# A. GNOME/系统级电源配置（如果可用）
PROFILE_SET_OK=0
if command -v powerprofilesctl >/dev/null 2>&1; then
  if run_root "powerprofilesctl set performance"; then
    PROFILE_SET_OK=1
    echo "powerprofilesctl -> performance"
  else
    echo "powerprofilesctl set failed, will fallback to cpufreq governor writes"
  fi
else
  echo "powerprofilesctl not found, skip"
fi

# B. cpufreq governor（把所有 policy 设置成 performance）
if [[ "${PROFILE_SET_OK}" -eq 1 ]]; then
  echo "cpufreq governor direct writes skipped (managed by power-profiles-daemon)"
  echo "Note: governor may still show 'powersave' on intel_pstate; check power profile/EPP/real freq instead."
else
  if compgen -G "/sys/devices/system/cpu/cpufreq/policy*/scaling_governor" >/dev/null; then
    run_root 'for f in /sys/devices/system/cpu/cpufreq/policy*/scaling_governor; do echo performance > "$f"; done'
    echo "cpufreq governor -> performance (all policies)"
  else
    echo "cpufreq policy files not found, skip"
  fi
fi
echo

echo "== 4) 可选：关闭 SMT/超线程 =="
if [[ "${DISABLE_SMT}" -eq 1 ]]; then
  if [[ -w /sys/devices/system/cpu/smt/control || "${EUID}" -ne 0 ]]; then
    run_root 'echo off > /sys/devices/system/cpu/smt/control'
    echo "SMT disabled"
  else
    echo "SMT control file not writable, skip"
  fi
else
  echo "SMT unchanged (pass --disable-smt to disable)"
fi
echo

echo "== 5) 识别 P-core / E-core（按 max MHz 粗分） =="
# 对 14900K 这类混合架构：maxmhz 高的一组通常是 P-core 线程
mapfile -t CPU_MAX_ROWS < <(lscpu -e=cpu,maxmhz | awk 'NR>1 && $1 ~ /^[0-9]+$/ {print $1","$2}')
if [[ "${#CPU_MAX_ROWS[@]}" -gt 0 ]]; then
  UNIQUE_MAX="$(printf '%s\n' "${CPU_MAX_ROWS[@]}" | cut -d, -f2 | sort -n | uniq)"
  FAST_MAX="$(printf '%s\n' "${UNIQUE_MAX}" | tail -n1)"
  P_CPU_LIST="$(printf '%s\n' "${CPU_MAX_ROWS[@]}" | awk -F, -v m="${FAST_MAX}" '$2==m{print $1}' | paste -sd, -)"
  E_CPU_LIST="$(printf '%s\n' "${CPU_MAX_ROWS[@]}" | awk -F, -v m="${FAST_MAX}" '$2!=m{print $1}' | paste -sd, -)"
else
  P_CPU_LIST=""
  E_CPU_LIST=""
fi

if [[ -n "${P_CPU_LIST}" ]]; then
  echo "P-core logical CPUs (estimated): ${P_CPU_LIST}"
else
  echo "P-core logical CPUs: unknown"
fi
if [[ -n "${E_CPU_LIST}" ]]; then
  echo "E-core logical CPUs (estimated): ${E_CPU_LIST}"
else
  echo "E-core logical CPUs: unknown"
fi
echo

echo "== 6) 建议实验绑核 =="
# 避免 reader 与 worker 落在同一物理核兄弟线程上
# 14900K 常见做法：reader 放 0，worker 放 2,4,6,...,14（先只用 P-core 的一半逻辑编号）
echo "Example (P-core only):"
echo "  taskset -c 0-15 ./mark1/build/ndpiBenchmark -i input/seed_10G.pcap -n 7 -r 0 -c 2,4,6,8,10,12,14 -q"
echo

if [[ "${WRITE_ENV_FILE}" -eq 1 ]]; then
  cat > "${ENV_FILE}" <<EOF
#!/usr/bin/env bash
# Auto-generated by scripts/prepare_benchmark_env.sh
# Source this file before benchmark:
#   source ${ENV_FILE}

# Estimated P-core logical CPU list by max MHz.
export BENCH_P_CPUS="${P_CPU_LIST}"
export BENCH_E_CPUS="${E_CPU_LIST}"

# Recommended worker CPU list (P-core first half logical IDs).
export BENCH_READER_CORE="0"
export BENCH_WORKER_CPUS="2,4,6,8,10,12,14"
EOF
  chmod +x "${ENV_FILE}"
  echo "Wrote env file: ${ENV_FILE}"
  echo "Use: source ${ENV_FILE}"
fi

echo
echo "== 7) 最终状态复核 =="
[[ -r /sys/devices/system/cpu/cpufreq/policy0/scaling_governor ]] && \
  echo "scaling_governor : $(cat /sys/devices/system/cpu/cpufreq/policy0/scaling_governor)"
[[ -r /sys/devices/system/cpu/cpufreq/policy0/energy_performance_preference ]] && \
  echo "EPP (policy0)    : $(cat /sys/devices/system/cpu/cpufreq/policy0/energy_performance_preference)"
[[ -r /sys/devices/system/cpu/intel_pstate/status ]] && \
  echo "intel_pstate     : $(cat /sys/devices/system/cpu/intel_pstate/status)"
if command -v powerprofilesctl >/dev/null 2>&1; then
  echo "power_profile    : $(powerprofilesctl get || true)"
fi
[[ -r /sys/devices/system/cpu/smt/control ]] && \
  echo "smt/control      : $(cat /sys/devices/system/cpu/smt/control)"
[[ -r /sys/devices/system/cpu/smt/active ]] && \
  echo "smt/active       : $(cat /sys/devices/system/cpu/smt/active)"

echo
echo "Done."
